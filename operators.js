//maths operators
const ageTaiwo = 2026 - 1993;
const ageTemy = 2024 - 1987;
console.log(ageTaiwo, ageTemy);

console.log(ageTaiwo * 12, ageTemy / 4, ageTaiwo ** 3);

//assignment operators
let shadesAge = 10;
shadesAge += 15; //shadeAge = shadeAge + 15 = 25
shadesAge *= 8; //shadeAge = shadeAge * 8 = 200
shadesAge++; //shadeAge = shadeAge + 1 = 201
shadesAge--; //shadeAge = shadeAge - 1 = 199

console.log(shadesAge);

//comparison operators
console.log(ageTaiwo > ageTemy); //false
console.log(ageTaiwo < ageTemy); //true
console.log(ageTaiwo >= ageTemy); //false
console.log(ageTaiwo <= ageTemy); //true
